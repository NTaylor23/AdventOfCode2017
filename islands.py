neighbors = [(0, 1), (1, 0), (-1, 0), (0, -1)]

def count_islands(grid) -> int:
    seen = set()

    def dfs(pos: tuple, num: int):
        stack = [pos]
        while stack:
            y, x = stack.pop()
            grid[y][x] = num
            seen.add((y, x))
            for n in neighbors:
                _y, _x = n[0] + y, n[1] + x
                is_in_bounds = 0 <= _y < size and 0 <= _x < size
                is_unseen = (_y, _x) not in seen
                is_set = grid[_y][_x] == 1
                if is_in_bounds and is_unseen and is_set:
                    stack.append((_y, _x))
        
    count = 1

    for i, row in enumerate(grid):
        for j, bit in enumerate(row):
            pos = (i, j)
            if bit == 1 and pos not in seen:
                dfs(pos, count)
                count += 1
    
    return count

